<h2>What is HTML?</h2>
<p>HTML is a <strong>markup</strong> language for <strong>describing</strong> web documents (web pages).</p>
<ul>
<li>HTML stands for <b>H</b>yper <b>T</b>ext <b>M</b>arkup <b>L</b>anguage</li>
	<li>A markup language is a set of <b>markup tags</b></li>
	<li>HTML documents are described by <b>HTML tags</b></li>
	<li>Each HTML tag <b>describes</b> different document content</li>
</ul>
<hr>

<h2>A Small HTML Document</h2>
<div class="w3-example">
<h3>Example</h3>
<div class="w3-code notranslate htmlHigh">
 &lt;!DOCTYPE html&gt;<br>
&lt;html&gt;<br>&lt;head&gt;<br>&lt;title&gt;Page Title&lt;/title&gt;<br>
 &lt;/head&gt;<br>
&lt;body&gt;<br><br>&lt;h1&gt;My First Heading&lt;/h1&gt;<br>&lt;p&gt;My first paragraph.&lt;/p&gt;<br><br>
    &lt;/body&gt;<br>&lt;/html&gt;
</div>
<a target="_blank" href="tryit6a1d.asp?filename=tryhtml_intro" class="w3-btn w3-margin-bottom">Try it&raquo;</a>
</div>
<h3>Example Explained</h3>
<ul>
<li>The <strong>&lt;!DOCTYPE html&gt;</strong> declaration defines this document to be HTML5</li>
<li>The text between <strong>&lt;html&gt;</strong> and <strong>&lt;/html&gt;</strong> describes an HTML document</li>
 <li>The text between <strong>&lt;head&gt;</strong> and <strong>&lt;/head&gt;</strong> provides information about the document</li>
 <li>The text between <strong>&lt;title&gt;</strong> and <strong>&lt;/title&gt;</strong> provides a title for the document</li>
 <li>The text between <strong>&lt;body&gt;</strong> and <strong>&lt;/body&gt;</strong> describes the visible page content</li>
 <li>The text between <strong>&lt;h1&gt;</strong> and <strong>&lt;/h1&gt;</strong> describes a heading</li>
 <li>The text between <strong>&lt;p&gt;</strong> and <strong>&lt;/p&gt;</strong> describes a paragraph</li>
</ul>
<p>Using this description, a web browser will display a document with a heading and a paragraph.</p>
<hr>

<h2>HTML Tags</h2>
<p>HTML tags are <strong>keywords</strong> (tag names) surrounded by <b>angle brackets</b>:</p>
<div class="htmlHigh" style="font-size:20px;padding:10px;margin-bottom:10px;word-wrap:break-word;font-family:Consolas,'courier new'">
&lt;tagname&gt;content goes here...&lt;/tagname&gt;
</div>
<ul>
<li>HTML tags normally come <b>in pairs</b> like &lt;p&gt; and &lt;/p&gt;</li>
<li>The first tag in a pair is the <b> start tag,</b> the second tag is the <b> end tag</b></li>
<li>The end tag is written like the start tag, but with a <strong>forward slash</strong> 
inserted before the tag name </li>
</ul>
<div class="w3-panel w3-note">
  <p>The start tag is also called the <b>opening tag</b>, and the end tag the <b>closing tag</b>.</p>
</div>
<hr>

<h2>Web Browsers</h2>
<p>The purpose of a web browser (Chrome, IE, Firefox, Safari) is to read HTML documents and display them.</p>
<p>The browser does not display the HTML tags, but uses them to determine how to display the document:</p>
<p><img alt="View in Browser" style="height:auto;width:635px" src="img_chrome.png" class="img-responsive"></p>
<hr>

<h2>HTML Page Structure</h2>
<p>Below is a visualization of an HTML page structure:</p>
<div style="width:99%;border:1px solid grey;padding:3px;margin:0;background-color:#ddd">&lt;html&gt;
<div style="width:90%;border:1px solid grey;padding:3px;margin:20px">&lt;head&gt;
<div style="width:90%;border:1px solid grey;padding:5px;margin:20px">&lt;title&gt;Page title&lt;/title&gt;
</div>
&lt;/head&gt;
</div>
<div style="width:90%;border:1px solid grey;padding:3px;margin:20px;background-color:#ddd">&lt;body&gt;
<div style="width:90%;border:1px solid grey;padding:3px;margin:20px;background-color:#fff">
<div style="width:90%;border:1px solid grey;padding:5px;margin:20px">&lt;h1&gt;This is a heading&lt;/h1&gt;</div>
<div style="width:90%;border:1px solid grey;padding:5px;margin:20px">&lt;p&gt;This is a paragraph.&lt;/p&gt;</div>
<div style="width:90%;border:1px solid grey;padding:5px;margin:20px">&lt;p&gt;This is another paragraph.&lt;/p&gt;</div>
</div>
&lt;/body&gt;
</div>
&lt;/html&gt;
</div>
<div class="w3-panel w3-note">
<p><strong>Note:</strong> Only the content inside the &lt;body&gt; section (the white area above) is displayed in a browser.</p>
</div>
<hr>

<h2>The &lt;!DOCTYPE&gt; Declaration</h2>
<p>The &lt;!DOCTYPE&gt; declaration represents the document type, and helps the browser to display a web page correctly.</p>
<p>It must only appear once, at the top of the page (before any HTML tags). </p>
<p>There are different document types. To display a web page correctly, the browser must know both type and version.</p>
<p>The doctype declaration is not case sensitive. All cases are acceptable:</p>
<div class="w3-example">
<div class="w3-code notranslate htmlHigh">
&lt;!DOCTYPE html&gt;<br><br>
&lt;!doctype HTML&gt;<br>
</div>
</div>
<hr>

